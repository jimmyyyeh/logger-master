import json


class MongoHandler:
    def __init__(self, mongo_instance, database, collection):
        self.mongo_instance = mongo_instance
        self.database = database
        self.collection = collection

    def insert_data(self, data):
        self.mongo_instance[self.database][self.collection].insert_one(json.loads(data))
